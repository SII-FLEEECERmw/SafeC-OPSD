# ============================================================
# Prompt 获取函数
# ============================================================

def get_judge_prompt(benchmark: str, mode: str) -> str:
    """
    根据 benchmark 和 judge mode 获取对应的 prompt 模板。

    Args:
        benchmark: benchmark 名称
        mode: judge 模式 (safe, effective, refusal, classification)

    Returns:
        prompt 模板字符串
    """
    prompt_map = {
        ("mssbench", "safe"): SAFETY_JUDGE_PROMPT,
        ("mssbench", "effective"): EFFECTIVENESS_JUDGE_PROMPT,
        ("mssembodied", "safe"): MSSEMBODIED_SAFETY_JUDGE_PROMPT,
        ("mssembodied", "effective"): EFFECTIVENESS_JUDGE_PROMPT,
        ("siuo", "safe"): SIUO_SAFETY_JUDGE_PROMPT,
        ("siuo", "effective"): EFFECTIVENESS_JUDGE_PROMPT,
        ("beavertails", "safe"): GENERAL_SAFETY_JUDGE_PROMPT,
        ("spavl", "safe"): GENERAL_SAFETY_JUDGE_PROMPT,
        ("vlguard", "safe"): VLGUARD_SAFETY_JUDGE_PROMPT,
        ("vlguard", "overrefusal"): VLGUARD_OVERREFUSAL_JUDGE_PROMPT,
        ("beavertails", "winrate_harm"): WINRATE_HARM_JUDGE_PROMPT,
        ("beavertails", "winrate_help"): WINRATE_HELP_JUDGE_PROMPT,
        ("spavl", "winrate_harm"): WINRATE_HARM_JUDGE_PROMPT,
        ("spavl", "winrate_help"): WINRATE_HELP_JUDGE_PROMPT,
        ("vlsbench", "refusal"): VLSBENCH_JUDGE_PROMPT,
    }

    key = (benchmark, mode)
    if key not in prompt_map:
        raise ValueError(
            f"No judge prompt for benchmark={benchmark}, mode={mode}. "
            f"Available: {list(prompt_map.keys())}"
        )
    return prompt_map[key]


def get_refusal_keywords(benchmark: str):
    """获取指定 benchmark 的拒绝关键词列表"""
    if benchmark == "vlguard":
        return VLGUARD_REFUSAL_KEYWORDS
    return REFUSAL_KEYWORDS
